# DOUBLESPIKE
#
# Files
#   cocktail               - Generate double spike cocktail lists
#   dsinversion            - Do the double spike inversion for a given set of measurements
#   dsstartup              - Double spike startup - defines a number default parameters in global ISODATA
#   errorcurve             - A plot of error as a function of double spike-sample proportions for a given double spike composition
#   errorcurve2            - A plot of error as a function of double spike proportions for a given double spike-sample proportion
#   errorcurve2d           - A 2D contour plot of error as a function of double spike composition and double spike-sample proportions
#   errorcurveoptimalspike - Find the optimal double spike compositions and plot the corresponding error curves
#   errorestimate          - Calculates the error in the natural fractionation factor or a chosen ratio by linear error propagation
#   example                - A brief demonstration of the double spike toolbox
#   monterun               - Generate a fake mass spectrometer run by Monte-Carlo simulation
#   optimalspike           - Find the optimal double spike composition and double spike-sample mixture proportions
#   seterrormodel          - Sets the coefficients of the error model in the global variable ISODATA
